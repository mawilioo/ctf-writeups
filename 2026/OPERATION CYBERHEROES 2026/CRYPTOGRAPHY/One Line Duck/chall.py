# One-Line Duck
# Give me the flag and I'll quack if it's correct.

MOD = 147573952589676412927
NUM_CONSTRAINTS = 24
TARGETS = [402712235, 1641913624228, 642033056552607, 67062383355890354, 3015890087404562875, 75621100142205097440, 54439766132355649047, 67501390084162708256, 142541596138675302568, 138738849737622044560, 94176950107903794750, 80251394051784004419, 72379374205401762317, 48774126549622494084, 39926213304781495935, 13537376142590932677, 97280306823067613871, 10483375554361384424, 58328316716263228943, 125554086513498291907, 82615529945542353023, 126943729914407991685, 39708635104512346187, 100672686568625571656]

x = input("flag: ").encode()

# The whole flag checker, compressed into one line:
assert re.fullmatch(rb"OPUCC\{[a-z0-9_?]{15}\}", x) and [
    sum(x[j] * pow(i + 2, j, MOD) for j in range(len(x))) % MOD
    for i in range(NUM_CONSTRAINTS)
] == TARGETS

print("Correct duck!")
